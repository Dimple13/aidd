<h3>My Child Has Been Diagnosed with ADHD – Now What?</h3>

<p>When their child is diagnosed with ADHD, parents often have concerns about deciding the best way to help their child. It is important for parents to remember that ADHD can be successfully managed. There are many treatment options, so parents should work closely with everyone involved in the child’s life—healthcare providers, therapists, teachers, coaches, and other family members. Taking advantage of all the resources available will help parents guide their child towards success.</p>
<p>For children 6 years of age and older, the American Academy of Pediatrics (AAP) recommends both behavior therapy and medication as good options, preferably both together. For young children (under 6 years of age) with ADHD, behavior therapy is recommended as the first line of treatment, before medication is tried. Good treatment plans will include close monitoring of whether and how much the treatment helps the child’s behavior, and making changes as needed along the way</p>
<h3>Behavior Therapy, Including Training for Parents</h3>
<h3> What is behavior therapy?</h3>
<p id="behave">Research shows that behavior therapy is an important part of treatment for children with ADHD. ADHD affects not only a child’s ability to pay attention or sit still at school, it also affects relationships with family and other children.  Children with ADHD often show behaviors that can be very disruptive to others. Behavior therapy is a treatment option that can help reduce these behaviors. It is often helpful to start behavior therapy as soon as a diagnosis is made.</P>

<p>The goals of behavior therapy are to learn or strengthen positive behaviors and eliminate unwanted or problem behaviors. Behavior therapy can include behavior therapy training for parents, behavior therapy with children, or a combination. Teachers can also use behavior therapy to help reduce problem behaviors in the classroom.</P>
<li class="list-group-item">In parent training in behavior therapy, parents learn new skills or strengthen their existing skills to teach and guide their children and to manage their behavior. Parent training in behavior therapy has been shown to strengthen the relationship between the parent and child, and to decrease children’s negative or problem behaviors. Parent training in behavior therapy is also known as behavior management training for parents, parent behavior therapy, behavioral parent training, or just parent training.</li>
<li class="list-group-item">In behavior therapy with children, the therapist works with the child to learn new behaviors to replace behaviors that don’t work or cause problems. The therapist may also help the child learn to express feelings in ways that do not create problems for the child or other people.</li>
